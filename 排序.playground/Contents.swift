func maopao(array: [Int]) {
    var arr: [Int] = array
    for i in 0..<arr.count {
        for j in 0..<arr.count-1-i {
            if arr[j]>arr[j+1] {
                arr.swapAt(j, j+1)
            }
        }
    }
    print("====  \(arr)")
}


